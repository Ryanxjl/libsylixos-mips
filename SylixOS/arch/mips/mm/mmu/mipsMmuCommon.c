#define  __SYLIXOS_KERNEL
#include "SylixOS.h"
#include "mipsMmuCommon.h"
/*********************************************************************************************************
** 函数名称: mipsGetAbtAddr
** 功能描述: MMU 系统发生数据访问异常时的地址
** 输　入  : NONE
** 输　出  : 数据异常地址
** 全局变量:
** 调用模块:
*********************************************************************************************************/
addr_t  mipsGetAbtAddr (VOID)
{
    return  ((addr_t)0ul);
}
/*********************************************************************************************************
** 函数名称: mipsGetAbtType
** 功能描述: MMU 系统发生数据访问异常时的类型
** 输　入  : NONE
** 输　出  : 数据异常类型
** 全局变量:
** 调用模块:
*********************************************************************************************************/
ULONG   mipsGetAbtType (VOID)
{
    return  (LW_VMM_ABORT_TYPE_TERMINAL);
}
/*********************************************************************************************************
** 函数名称: mipsGetPreAddr
** 功能描述: MMU 系统发生指令访问异常时的地址
** 输　入  : NONE
** 输　出  : 指令异常地址
** 全局变量:
** 调用模块:
*********************************************************************************************************/
addr_t  mipsGetPreAddr (addr_t  ulRetLr)
{
    return  (ulRetLr);
}
/*********************************************************************************************************
** 函数名称: mipsGetPreType
** 功能描述: MMU 系统发生指令访问异常时的类型
** 输　入  : NONE
** 输　出  : 指令异常类型
** 全局变量:
** 调用模块:
*********************************************************************************************************/
ULONG   mipsGetPreType (VOID)
{
    return  (LW_VMM_ABORT_TYPE_TERMINAL);
}
